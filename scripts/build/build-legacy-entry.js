const fs = require('node:fs');
const path = require('node:path');

const projectRoot = path.resolve(__dirname, '..', '..');
const generatedDir = path.join(projectRoot, 'src', 'generated');
const outputPath = path.join(generatedDir, 'generated-app.jsx');
const adminOutputPath = path.join(generatedDir, 'generated-admin.jsx');

const legacyAppScripts = [
  'src/core/tweaks-panel.jsx',
  'src/core/clerk-auth.jsx',
  'src/core/math-loader.js',
  'src/core/backend-api.jsx',
  'src/core/shared.jsx',
  'src/core/onboarding.jsx',
  'src/core/route-prefetch.js',
  'src/pages/lobby.jsx',
  'src/pages/belajar.jsx',
  'src/pages/profile.jsx',
  'src/pages/invoices.jsx',
  'src/features/practice/toolbar.jsx',
  'src/features/practice/drawing-canvas.jsx',
  'src/features/practice/answer-board.jsx',
  'src/features/practice/practice.jsx',
  'src/pages/misi.jsx',
  'src/pages/tryout.jsx',
  'src/pages/leaderboard.jsx',
  'src/pages/payment.jsx',
  'src/core/app.jsx',
];

const adminScripts = [
  'src/features/admin/admin-monitoring.jsx',
  'src/features/admin/admin.jsx',
];

const prelude = `// Generated by scripts/build/build-legacy-entry.js. Do not edit by hand.
import React from 'react';
import { createPortal } from 'react-dom';
import { createRoot } from 'react-dom/client';

const ReactDOM = { createRoot, createPortal };
const katex = new Proxy({}, {
  get(_target, property) {
    const instance = window.katex;
    if (!instance) throw new Error('KaTeX belum dimuat.');
    const value = instance[property];
    return typeof value === 'function' ? value.bind(instance) : value;
  }
});

window.React = React;
window.ReactDOM = ReactDOM;
`;

const adminPrelude = `// Generated by scripts/build/build-legacy-entry.js. Do not edit by hand.
const React = window.React;
const ReactDOM = window.ReactDOM;
const MafikingAPI = window.MafikingAPI;
const Icon = window.Icon;
const showToast = window.showToast;
`;

function concatenateScripts(scripts) {
  return scripts.map((relativePath) => {
    const absolutePath = path.join(projectRoot, relativePath);
    const source = fs.readFileSync(absolutePath, 'utf8');
    return `\n\n/* ${relativePath} */\n${source}`;
  }).join('');
}

const body = concatenateScripts(legacyAppScripts);
const adminBody = concatenateScripts(adminScripts);
fs.mkdirSync(generatedDir, { recursive: true });
fs.writeFileSync(outputPath, `${prelude}${body}\n`);
fs.writeFileSync(adminOutputPath, `${adminPrelude}${adminBody}\n`);
console.error(`Generated ${path.relative(projectRoot, outputPath)} from ${legacyAppScripts.length} legacy scripts.`);
console.error(`Generated ${path.relative(projectRoot, adminOutputPath)} from ${adminScripts.length} admin scripts.`);
