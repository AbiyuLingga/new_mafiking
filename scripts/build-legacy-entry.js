const fs = require('node:fs');
const path = require('node:path');

const projectRoot = path.resolve(__dirname, '..');
const outputPath = path.join(projectRoot, 'src', 'generated-app.jsx');

const legacyAppScripts = [
  'tweaks-panel.jsx',
  'src/clerk-auth.jsx',
  'src/backend-api.jsx',
  'src/shared.jsx',
  'src/onboarding.jsx',
  'src/lobby.jsx',
  'src/belajar.jsx',
  'src/profile.jsx',
  'src/toolbar.jsx',
  'src/drawing-canvas.jsx',
  'src/answer-board.jsx',
  'src/practice.jsx',
  'src/misi.jsx',
  'src/tryout.jsx',
  'src/leaderboard.jsx',
  'src/payment.jsx',
  'src/admin-monitoring.jsx',
  'src/admin.jsx',
  'src/app.jsx',
];

const prelude = `// Generated by scripts/build-legacy-entry.js. Do not edit by hand.
import React from 'react';
import { createPortal } from 'react-dom';
import { createRoot } from 'react-dom/client';

const ReactDOM = { createRoot, createPortal };
const katex = new Proxy({}, {
  get(_target, property) {
    const instance = window.katex;
    if (!instance) throw new Error('KaTeX belum dimuat.');
    const value = instance[property];
    return typeof value === 'function' ? value.bind(instance) : value;
  }
});

window.React = React;
window.ReactDOM = ReactDOM;
`;

const body = legacyAppScripts.map((relativePath) => {
  const absolutePath = path.join(projectRoot, relativePath);
  const source = fs.readFileSync(absolutePath, 'utf8');
  return `\n\n/* ${relativePath} */\n${source}`;
}).join('');

fs.writeFileSync(outputPath, `${prelude}${body}\n`);
console.error(`Generated ${path.relative(projectRoot, outputPath)} from ${legacyAppScripts.length} legacy scripts.`);
